# (1~1) ~ (2~7) ~ (8~19) ~ (20~37) ~ (38~61)
# 1 6 12 18 24 .. 
# +A // +6A // +12A :::::A*(x-1)*6
# 13 -> 3 // 8 -> 3 // 20 -> 4 
# 12 -> 2// 7 ->1.XX// 19 -> 3.XX   (1을 뺴고 6으로 나눴을 때 올림)
# ~ 1+6 //~ 1+6+12

N = int(input())

base = 1
num = 0
while (N > base + 6*num):
  base += 6*num
  num+=1
result = 1 + num

print(result)
